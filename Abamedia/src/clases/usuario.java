package clases;

public class usuario {
	private String nombreU;
	private String contra;
	
	public usuario ()
	{
	}
	
	public String getNombreU ()
	{
		return nombreU;
	}
	
	public String getContra()
	{
		return contra;
	}	
	
	public void setNombreU (String pNombre)
	{
		nombreU = pNombre;
	}
	
	public void setContra (String pContra)
	{
		contra = pContra;
	}
}
